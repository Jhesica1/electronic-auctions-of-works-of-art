    private void BTN_BUSCAR_EXPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_BUSCAR_EXPActionPerformed
        // TODO add your handling code here:
        correo = CORREO_EXPOSITOR.getText().trim();
    
    if (correo.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Por favor ingrese un correo", 
            "Campo vac√≠o", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        DefaultTableModel model = (DefaultTableModel) TABLA_BUSCAR.getModel();
        model.setRowCount(0); // Limpia la tabla antes de buscar
        
        // Contador para obras encontradas
        AtomicInteger contador = new AtomicInteger(0);
        
        archiObra.buscarObra(correo, obra -> {
            // A√±ade cada obra a la tabla
            model.addRow(new Object[]{
                obra.getCorreo(),
                obra.getAutor(),
                obra.getPrecioBase(),
                obra.getEstadoCompra(),
                obra.getDetalle()
            });
            contador.incrementAndGet();
        });
        
        // Muestra feedback al usuario
        if (contador.get() == 0) {
            JOptionPane.showMessageDialog(this,
                "No se encontraron obras para el correo: " + correo,
                "Sin resultados", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this,
                "Se encontraron " + contador.get() + " obra(s)",
                "Resultados", JOptionPane.INFORMATION_MESSAGE);
        }
        
    } catch (IOException | ClassNotFoundException e) {
        JOptionPane.showMessageDialog(this,
            "Error al acceder a los datos: " + e.getMessage(),
            "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
    }//GEN-LAST:event_BTN_BUSCAR_EXPActionPerformed

